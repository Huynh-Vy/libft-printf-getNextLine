#include "libft.h"
// Define whether the character is alphanumeric or not
int	ft_isalnum(int i)
{
	if ((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z') 
		|| (i >= '0' && i <= '9'))
		return (1);
	return (0);
}
